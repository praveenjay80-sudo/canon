// Run: node scripts/crawl-topsci-facets.mjs
// One full pass over pasanhu.cn's "World's Top 2% Scientists" 2024 single-year
// list (field/subfield-1/subfield-2/country columns only) to derive the
// Field -> Subfield taxonomy (Science-Metrix classification, 22 fields /
// 174 subfields) and the set of country codes actually present.
// Outputs: src/constants/topSciFacets.js

import { writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = join(__dirname, '../src/constants/topSciFacets.js');
const BASE = 'https://www.pasanhu.cn';
const UA = 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36';
// pasanhu.cn's ASMX service 500s above ~500-800 rows/page for a 4-column
// query (its response-size limit scales with column count) — 500 is safe.
const PAGE_SIZE = 500;
const CONCURRENCY = 8;

async function getToken() {
  const r = await fetch(`${BASE}/WorldTopScientists.aspx`, { headers: { 'User-Agent': UA } });
  const html = await r.text();
  const m = html.match(/main\.render\("([^"]+)"/);
  if (!m) throw new Error('Could not extract auth token from page');
  return m[1];
}

async function queryPage(token, page, attempt = 0) {
  try {
    const r = await fetch(`${BASE}/HSSrv.asmx/QueryWPData2`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json;charset=utf-8', 'Authorization': token, 'User-Agent': UA },
      body: JSON.stringify({
        year: '2024', type: '', authfull: '', inst_name: '', cntry: '',
        sm_field: '', sm_subfield_1: '', sm_subfield_2: '',
        page, limit: PAGE_SIZE, column: ['cntry', 'sm-field', 'sm-subfield-1', 'sm-subfield-2'],
      }),
    });
    if (!r.ok) throw new Error(`HTTP ${r.status}`);
    const json = await r.json();
    if (json?.d?.code !== 0) throw new Error(json?.d?.msg || 'API error');
    return json.d;
  } catch (e) {
    if (attempt < 5) {
      await new Promise(res => setTimeout(res, 2000 * (attempt + 1)));
      return queryPage(token, page, attempt + 1);
    }
    throw e;
  }
}

async function main() {
  const t0 = Date.now();
  console.log('Crawling field/subfield/country facets from pasanhu.cn...\n');

  const token = await getToken();
  const first = await queryPage(token, 1);
  const total = first.count;
  const totalPages = Math.ceil(total / PAGE_SIZE);
  console.log(`${total.toLocaleString()} scientists across ${totalPages} pages\n`);

  const fieldSubfields = new Map(); // field -> Set(subfield)
  const allSubfields = new Set();
  const countries = new Set();

  function ingest(rows) {
    for (const row of rows) {
      const [cntry, field, sf1, sf2] = row.values;
      if (cntry) countries.add(cntry);
      if (field) {
        if (!fieldSubfields.has(field)) fieldSubfields.set(field, new Set());
        if (sf1) { fieldSubfields.get(field).add(sf1); allSubfields.add(sf1); }
      }
      if (sf2) allSubfields.add(sf2);
    }
  }
  ingest(first.data);

  let done = 1;
  for (let batchStart = 2; batchStart <= totalPages; batchStart += CONCURRENCY) {
    const batch = [];
    for (let p = batchStart; p < batchStart + CONCURRENCY && p <= totalPages; p++) batch.push(p);
    const results = await Promise.all(batch.map(p => queryPage(token, p)));
    for (const d of results) ingest(d.data);
    done += batch.length;
    process.stdout.write(`\r  ${done}/${totalPages} pages`);
    await new Promise(r => setTimeout(r, 200));
  }
  console.log('\n');

  const FIELDS = [...fieldSubfields.keys()].sort();
  const FIELD_SUBFIELDS = {};
  for (const f of FIELDS) FIELD_SUBFIELDS[f] = [...fieldSubfields.get(f)].sort();
  const ALL_SUBFIELDS = [...allSubfields].sort();
  const COUNTRIES = [...countries].sort();

  console.log(`Fields: ${FIELDS.length}`);
  console.log(`Subfields (union of subfield-1 + subfield-2): ${ALL_SUBFIELDS.length}`);
  console.log(`Countries: ${COUNTRIES.length}`);

  const output = `// Auto-generated by scripts/crawl-topsci-facets.mjs — do not hand-edit
// Derived from pasanhu.cn's live "World's Top 2% Scientists" 2024 list
// (Science-Metrix classification). Crawled: ${new Date().toISOString()}

export const TOPSCI_FIELDS = ${JSON.stringify(FIELDS, null, 2)};

export const TOPSCI_FIELD_SUBFIELDS = ${JSON.stringify(FIELD_SUBFIELDS, null, 2)};

export const TOPSCI_ALL_SUBFIELDS = ${JSON.stringify(ALL_SUBFIELDS, null, 2)};

export const TOPSCI_COUNTRIES = ${JSON.stringify(COUNTRIES, null, 2)};
`;

  writeFileSync(OUT, output, 'utf8');
  console.log(`\nWrote ${OUT}`);
  console.log(`Elapsed: ${Math.round((Date.now() - t0) / 1000)}s`);
}

main().catch(e => { console.error(e); process.exit(1); });
